
import PolygonWrapper as pw
from datetime import datetime, timedelta
import value

def iterate_prices(ticker, entry_price, start, sl, tp):
    print("iterating prices")
    last_price = 0
    last_date = 0
    end = datetime.strptime(start, '%Y-%m-%d') + timedelta(days=720)
    end = end.strftime('%Y-%m-%d')
    candles = pw.get_aggregates(ticker, start, end)
    for i in range(0,len(candles)-1):
        date = datetime.strptime(start, '%Y-%m-%d') + timedelta(days=i)
        date = date.strftime('%Y-%m-%d')
        last_date = date
        candle = candles[i]
        if candle.get('c'):
            current_price = candle['c']
            last_price = current_price
            change = (current_price - entry_price) / entry_price
            print(date + " -- " + str(candle.get('c')) + " -- " + str(change))
            if change > 0:
                if change > tp:
                    print("hit tp")
                    return (current_price, date)
            elif abs(change) > sl:
                print("hit sl")
                return (current_price, date)
    print("360 days ran out -- exiting")
    return (last_price, last_date)
        
def backtest(level, ticker):
    balance = 1000
    fundamentals = pw.get_fundamentals(ticker, limit=50)['results'][::-1] #Quarterly -- 10 years
    if len(fundamentals) > 0:
        exit_date = fundamentals[0]['calendarDate']
        for i in range(5, len(fundamentals)-1):
            fundamental = fundamentals[i]
            fund_window = fundamentals[i-5:i]
            ticker = fundamental['ticker']
            date = fundamental['calendarDate']
            datetime_date = datetime.strptime(date, '%Y-%m-%d')
            datetime_exit_date = datetime.strptime(exit_date, '%Y-%m-%d')
            if datetime_date >= datetime_exit_date:
                price = 0
                for j in range(0,3):
                    date = datetime_date + timedelta(days=j)
                    date = date.strftime('%Y-%m-%d')
                    price = pw.get_candle_by_date(ticker, date).get('close')
                    if price:
                        break
                if price and value.get_check_for_buy_backtest(level, ticker, fund_window):
                    print(str(price) + " is buy")
                    exit_info = iterate_prices(ticker, price, date, .4, .5)
                    print(exit_info)
                    if exit_info:
                        exit_date = exit_info[1]
                        change = (exit_info[0] - price)/price
                        print(change)
                        balance = balance * (1+change)        
    return balance
            
